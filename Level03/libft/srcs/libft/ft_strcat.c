#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	n;

	i = -1;
	n = -1;
	while (s1[++i] != '\0')
		;
	while (s2[++n] != '\0')
	{
		s1[i] = s2[n];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
