#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
		return (0);
	else if (s[0] == '\0')
		return (0);
	while (s[i] != '\0')
		i++;
	return (i);
}
